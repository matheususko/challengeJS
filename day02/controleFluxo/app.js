// switch statements
const grade = 'B'

switch (grade) {
    case 'A':
        console.log('Você tirou A')
        break
    case 'B':
        console.log('Você tirou B')
        break
    case 'C':
        console.log('Você tirou C')
        break
    case 'D':
        console.log('Você tirou D')
        break
    case 'E':
        console.log('Você tirou E')
        break
    default:
        console.log('nota inválida')    
}


